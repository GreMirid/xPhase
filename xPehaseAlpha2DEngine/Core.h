
namespace xphase
{
	//Double float
	struct v2d
	{
		double x, y;
	};

	//Float
	struct v2f
	{
		float x, y;
	};

	//Long
	struct v2l
	{
		long x, y;
	};

	//Unsigned Long
	struct v2ul
	{
		unsigned long x, y;
	};

	//Int
	struct v2i
	{
		int x, y;
	};

	//Unsigned Int
	struct v2ui
	{
		unsigned int x, y;
	};

	//Short
	struct v2s
	{
		short x, y;
	};
	
	//Unsigned short
	struct v2us
	{
		unsigned short x, y;
	};
}